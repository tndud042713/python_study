#문제 (내가 푼 방법)
print("%d%s %.0f%c %s"%(2020,"년 08월",12,'일',"수요일"),"입니다")

#강사 풀이
print("%d%s %f%c %s"%(2020,"년 08월",12.0,'일',"수요일 입니다"))

print("%f"%(1.234))
print("%.1f"%(1.234))
print("%.2f"%(1.234))
print("%.3f"%(1.234))
print("%.0f"%(1.234))

print("%010.2f"%(1.234))
print("%-10.2f"%(1.234))
print("%-010.2f"%(1.234))

print("%02d월"%(1))
print("%02d월"%(9))
print("%02d월"%(10))

print("%10f"%(123456.7891234))